//Find if the input character is ‘a’ or not.

#include<iostream>
using namespace std;

int main(){
    char ch;
    cout<<"Enter any character in lowercase"<<endl;
    cin>>ch;

    if(ch == 'a'){
        cout<<"Input character is a"<<endl;
    }
    else{
        cout<<"Input character is not a"<<endl;
    }
}
